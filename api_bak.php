function checkIpPool($real_ip, $ip_pool)
{
    $split = explode(".", $real_ip);
    if(isset($split[0]) && $split[1]){
        $prefix_ip = $split[0] . "." . $split[1];
        foreach ($ip_pool as $ip){
            if (strpos($ip, $prefix_ip) !== false) {
                return true;
            }
        }
    }
    return false;
}

